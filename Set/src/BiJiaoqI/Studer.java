package BiJiaoqI;

public class Studer {
    private String name;
    private int age;

    public Studer(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public Studer() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }


}


